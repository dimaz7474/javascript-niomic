let nilai = 75; // Ganti dengan input nilai

if (nilai >= 90) {
  console.log("Nilai A " + nilai + " - Sangat Baik");
} else if (nilai >= 80) {
  console.log("Nilai B " + nilai + " - Baik");
} else if (nilai >= 70) {
  console.log("Nilai C " + nilai + " - Cukup");
} else if (nilai >= 60) {
  console.log("Nilai D " + nilai + " - Kurang");
} else {
  console.log("Nilai E " + nilai + " - Sangat Kurang");
}
