
// script javascript Buatlah 3 buah variabel menggunakan var , let dan const
// Variabel var berisikan nama depan teman teman , variabel let berisikan nama akhir teman teman , 

// const berisikan umur teman teman saat ini
// Setelah itu teman teman tampilkan ketiga variabel tersebut dengan console.log()


// Deklarasi variabel
var namaDepan = "Dimas";
let namaAkhir = "Kurniawan";
const umur = 37;

// Menampilkan variabel ke console
console.log("Nama Depan: " + namaDepan);
console.log("Nama Akhir: " + namaAkhir);
console.log("Umur: " + umur);