// Deklarasi array
var buah = ["Apel", "Jeruk", "Mangga"];
let angka = [1, 2, 3, 4, 5];
const campuran = ["Teks", 123, true, null];
// Menampilkan isi array
console.log("Buah-buahan:", buah);
console.log("Angka:", angka);
console.log("Campuran:", campuran);
// Akses elemen array
console.log("Buah pertama:", buah[0]);
console.log("Jumlah buah:", buah.length);
// Menambahkan elemen
buah.push("Pisang");
console.log("Buah setelah ditambah:", buah);
// Menghapus elemen terakhir
buah.pop();
console.log("Buah setelah dihapus:", buah);
