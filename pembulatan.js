// Contoh penggunaan Math.ceil, Math.floor, dan Math.round

const angka1 = 4.3;
const angka2 = 7.8;

// Math.ceil: membulatkan ke atas
console.log(Math.ceil(angka1)); // Output: 5
console.log(Math.ceil(angka2)); // Output: 8

// Math.floor: membulatkan ke bawah
console.log(Math.floor(angka1)); // Output: 4
console.log(Math.floor(angka2)); // Output: 7

// Math.round: membulatkan ke bilangan bulat terdekat
console.log(Math.round(angka1)); // Output: 4
console.log(Math.round(angka2)); // Output: 8