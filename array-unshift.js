// Contoh penggunaan unshift untuk menambah elemen di awal array

let buah = ["apel", "jeruk", "mangga"];
console.log("Sebelum unshift:", buah);

// Menambahkan satu elemen di awal array
buah.unshift("pisang");
console.log("Setelah unshift satu elemen:", buah);

// Menambahkan beberapa elemen sekaligus di awal array
buah.unshift("anggur", "melon");
console.log("Setelah unshift beberapa elemen:", buah);