const fruits = ['pisang', 'jeruk', 'apel', 'mangga'];
console.log(fruits); // Output: ['pisang', 'jeruk', 'apel', 'mangga']

fruits.pop(); // Menghapus elemen terakhir
console.log(fruits); // Output: ['pisang', 'jeruk', 'apel'] 
fruits.shift(); // Menghapus elemen pertama
console.log(fruits); // Output: ['jeruk', 'apel']
