// Panjang sisi tegak dan sisi datar
let a = 6; // sisi tegak
let b = 8; // sisi datar

// Hitung sisi miring menggunakan Math.pow() dan Math.sqrt()
let sisiMiring = Math.sqrt(Math.pow(a, 2) + Math.pow(b, 2));

// Tampilkan hasil
console.log("Sisi tegak (a):", a);
console.log("Sisi datar (b):", b);
console.log("Panjang sisi miring (c):", sisiMiring);
